public class CharacterEx1 {
	public static void main(String[] args) {
		char ch1 = 'A';
		System.out.println(ch1);
		System.out.println((int)ch1);//업캐스팅
		
		char ch2 =66;
		System.out.println(ch2);
		
		int ch3 =67;
		System.out.println(ch3);
		System.out.println((char)ch3);//다운캐스팅
	}
}
